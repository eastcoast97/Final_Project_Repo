    private void populateNGOcombo() {
        
        ArrayList<NGO> ngo = ngodir.getNgolist();
        DefaultComboBoxModel df = new DefaultComboBoxModel();
        for(NGO n: ngo)
        {
            df.addElement(n.getNGOName());
        }
        ngocmbbx.setModel(df);
    }
