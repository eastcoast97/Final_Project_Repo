    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String ngoName = (String) ngocmbbx.getSelectedItem();
        NGO ngo = ngodir.getNGO(ngoName);
        DefaultTableModel df = (DefaultTableModel) sponsortbl.getModel();
        int selectedRow = sponsortbl.getSelectedRow();
        if(selectedRow == -1) {JOptionPane.showMessageDialog(this, "Select company");return;}
        String compName = df.getValueAt(selectedRow,0).toString();
        Company comp = ngodir.getCompany(compName,ngoName);
        
        Sponsor sponsor = sponsordir.getSponsor(ua);
        sponsordir.offerToCompany(sponsor, comp, Integer.parseInt(sponsortf.getText()));
        sponsortf.setText("");
    }//GEN-LAST:event_jButton1ActionPerformed

